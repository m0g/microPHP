<?php
class Session {
	/*==========================================================================
		Start new session
	==========================================================================*/
	public function __construct() {
		session_start();
	}

	/*==========================================================================
		Retrieve desired session var
	==========================================================================*/
	public function get ($key) {
		return isset($_SESSION[$key]) ? $_SESSION[$key] : false;
	}

	/*==========================================================================
		Save session var
	==========================================================================*/
	public function set ($key, $value) {
		$_SESSION[$key] = $value;
	}
}